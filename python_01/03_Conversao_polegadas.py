# Faça um programa em Python3 que receba um valor em polegadas,
# converta e imprima o resultado em milímetros.

print("------------ CONVERSÃO POLEGADAS -> MM ------------")

polegada = float(input("Digite uma polegada: "))

print(f"{polegada} polegada(s) é o mesmo que {polegada * 25.4:.2f} mm")

